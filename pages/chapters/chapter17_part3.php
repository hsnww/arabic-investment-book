                        <h5 class="text-primary mt-4 mb-3">
                            <i class="fas fa-arrow-up me-2"></i>
                            1. المنحنى الصاعد (Normal Yield Curve)
                        </h5>
                        <p>الشكل الطبيعي للمنحنى هو أن يكون صاعداً، أي أن العائد على أدوات الدين قصيرة الأجل (كشهادات الإيداع وأذونات الخزينة) أقل من ذلك المتحقق عن طريق أدوات الدين طويلة الأجل (كالسندات الطويلة). ويدل ذلك على أن الاقتصاد يمر بمرحلة نمو طبيعية ولا يوجد هناك تخوف من تضخم غير عادي في الأسعار أو معدلات البطالة وغيرها.</p>

                        <h5 class="text-success mt-4 mb-3">
                            <i class="fas fa-arrow-up me-2"></i>
                            2. المنحنى الصاعد الحاد (Steep Yield Curve)
                        </h5>
                        <p>أما إذا أتى المنحنى بشكل صاعد حاد، فإن ذلك يؤخذ غالباً على أن الاقتصاد مقبل على حركة ازدهار ونمو مما قد يضطر البنك المركزي إلى رفع معدل الفائدة، وبالتالي تكون السندات طويلة الأجل غير جذابة.</p>

                        <h5 class="text-danger mt-4 mb-3">
                            <i class="fas fa-arrow-down me-2"></i>
                            3. المنحنى الهابط (Inverted Yield Curve)
                        </h5>
                        <p>أما إذا كان شكل المنحنى هابطاً (ويسمى Inverted)، أي أن العائد على أدوات الدين قصيرة الأجل أعلى من العائد على أدوات الدين طويلة الأجل، وهو أقل حدوثاً من أشكال المنحنيات الأخرى. فيدل ذلك على أن التوقعات تشير إلى احتمال لجوء البنك المركزي إلى خفض معدلات الفائدة في الفترات القادمة، بسبب تخوفه من حدوث تراجع أو انحسار في الاقتصاد.</p>

                        <h5 class="text-warning mt-4 mb-3">
                            <i class="fas fa-minus me-2"></i>
                            4. المنحنى المفلطح (Flat Yield Curve)
                        </h5>
                        <p>أخيراً هناك أوقات يكون فيها المنحنى شبه مستقيم (أو مفلطح)، مما يعني أن معدلات الفائدة متشابهة ولا يبدو هناك توجه واضح لحالة الاقتصاد. ولا تستمر هذه الحالة طويلاً حيث سرعان ما تظهر مؤشرات تبين التوجه العام للاقتصاد والوجهة التي ستسلكها معدلات الفائدة.</p>

